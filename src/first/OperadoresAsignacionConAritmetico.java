public class OperadoresAsignacionConAritmetico {
	public static void main(String[] args) {
		int a = 10;
		
		a += 5;
		System.out.println(a);
		a -= 3;
		System.out.println(a);
		a *= 7;
		System.out.println(a);
		a /= 5;
		System.out.println(a);
		a %= 3;
		System.out.println(a);
		a ++;
		System.out.println(a);
		a --;
		System.out.println(a);
	}

}
